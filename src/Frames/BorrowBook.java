    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DateFormat dFormat = new SimpleDateFormat("d-MMM-y");
        for (BookInfo a : Book.arraylist) {
            if (a.getId() == Integer.parseInt(jTextField1.getText())) {

                Book.rBook.add(new BookInfo(a.getId(),a.getName(), a.getAuthor(), a.getBook_category(), a.getPage()));
                Book.delBook(a.getId());
                
                for (StudentInfo b : Student.arraylist) {
                    if (b.getId() == Integer.parseInt(jTextField2.getText())) {
                        JOptionPane.showMessageDialog(this,"Book Name : "+a.name + " \n" +"Student Name : "+ b.name + " \n" +"Borrow Date : "+ dFormat.format(jDateChooser1.getDate()) +"\n"+ "Due Date : " + dFormat.format(jDateChooser2.getDate()));
                    }
                }
                break;

            }
            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

