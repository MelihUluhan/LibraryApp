    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        for (BookInfo i : Book.rBook) {
            int m = 0;

            if (jComboBox3.getSelectedItem().equals(rBook.get(m).getId())) {
                Book.arraylist.add(new BookInfo(i.getId(),i.getName(), i.getAuthor(), i.getBook_category(), i.getPage()));
            }
            m++;
        }
        try {
            int index = -1;
            for (int i = 0; i < rBook.size(); i++) {
                if (jComboBox3.getSelectedItem().equals(rBook.get(i).getId())) {
                    index = i;
                }

            }
            if (index != -1) {
                rBook.remove(index);
            }

        } catch (Exception e) {
            System.out.println("");
        }
        JOptionPane.showMessageDialog(this,"Book added to library");

        
    }//GEN-LAST:event_jButton2ActionPerformed

